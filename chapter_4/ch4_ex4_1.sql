-- |        109 |           5 |   6.99 | 2005-05-31         |
-- |        110 |           5 |   1.99 | 2005-05-31         |
-- |        111 |           5 |   3.99 | 2005-06-15         |
-- |        112 |           5 |   2.99 | 2005-06-16         |
