-- 
-- `customer_id <> 5 AND (amount > 8 OR date(payment_date) = '2005-08-23')`
--
-- +------------+-------------+--------+--------------------+
-- | payment_id | customer_id | amount | date(payment_date) |
-- +------------+-------------+--------+--------------------+
-- |        101 |           4 |   8.99 | 2005-08-18         |
-- |        102 |           4 |   1.99 | 2005-08-19         |
-- |        103 |           4 |   2.99 | 2005-08-20         |
-- |        104 |           4 |   6.99 | 2005-08-20         |
